public class StringExercise {
    public static void main(String[] args){
        String noEducation = "We don't need no education\n" +
                "We don't need no thought control";
        String quotesInQuotes = "Check \"this\" out!, \"s inside of \"s!";
        String whereMainDrive = "In windows, the main drive is usually C:\\";
        String iCanDoBackSlashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(noEducation);
        System.out.println(quotesInQuotes);
        System.out.println(whereMainDrive);
        System.out.println(iCanDoBackSlashes);
    }
}
